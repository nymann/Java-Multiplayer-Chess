public class Pieces extends Board {
    final int ES = 0;   // Empty square
    final int WP = 1;   // White pawn
    final int WR = 2;   // White rook
    final int WN = 3;   // White knight (note abbreviated WN because of White King)
    final int WB = 4;   // White bishop
    final int WQ = 5;   // White queen
    final int WK = 6;   // White king
    final int BP = -1;  // Black pawn
    final int BR = -2;  // Black rook
    final int BN = -3;  // Black knight (note abbreviated WN because of Black King)
    final int BB = -4;  // Black bishop
    final int BQ = -5;  // Black queen
    final int BK = -6;  // Black king


}
